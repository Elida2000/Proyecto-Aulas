class ExtrasObj:
    def _init_(
        self, id, sonido, orador, sillas, banquete, plumones, aireacondicionado, otros,
    ):
        self.id = id
        self.sonido = sonido
        self.orador = orador
        self.sillas = sillas
        self.banquete = banquete
        self.plumones = plumones
        self.aireacondicionado = aireacondicionado
        self.otros = otros

    def idextras(self):
        extras = self.idextras
        return extras

